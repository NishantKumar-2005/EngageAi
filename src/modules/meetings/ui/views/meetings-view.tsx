"use client";

// import { ErrorState } from "src/components/error-state";
import { LoadingState } from "src/components/loading-state";
import { useTRPC } from "src/trpc/client";
import { useSuspenseQuery } from "@tanstack/react-query";
import ErrorState from "src/components/error-state";
import { DataTable } from "src/components/data-table";
import { columns } from "../components/columns";
import { EmptyState } from "src/components/empty-state";
import { useRouter } from "next/navigation";
import { useMeetingsFilters } from "../../hooks/use-meetings-filters";
import { DataPagination } from "src/modules/agents/ui/components/data-pagination";



export const MeetingsView = () => {
    const trpc = useTRPC();
    const router = useRouter();
    const [filters, setFilters] = useMeetingsFilters();
    const { data } = useSuspenseQuery(trpc.meetings.getMany.queryOptions({
      ...filters,
    }));

    
    

    return(
        <div className="flex-1 pb-4 px-4 md:px-8 flex flex-col gap-y-4 ">
          <DataTable data={data.items} columns={columns} onRowClick={(row)=>{ router.push(`/meetings/${row.id}`)}}/>
          <DataPagination
            page={filters.page}
            totalPages={data.totalPages}
            onPageChange={(page) => setFilters({ page })}
          />
            {data.items.length === 0 && (
                  <EmptyState
                    title="Create your first meeting"
                    description="Schedule a meeting to connect with your agents.Each meeting lets you collaborate,share ideas and interact with your agents in real-time."
                  />
                )}
        </div>
    );
  };

  export const MeetingsViewLoading = () => {
    return <LoadingState title="Loading Meetings" description="This may take a few seconds" />;
  };
  
  export const MeetingsViewError = () => {
    return (
      <ErrorState
        title="Error loading Meetings"
        description="Something went wrong while loading the agents"
      />
    );
  };